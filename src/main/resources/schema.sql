DROP TABLE IF EXISTS USERS;
DROP TABLE IF EXISTS TWEETS;
DROP TABLE IF EXISTS FOLLOWS;

CREATE TABLE USERS (
  user_id INT AUTO_INCREMENT  PRIMARY KEY,
  user_name VARCHAR(140) NOT NULL,
  age INT NOT NULL
);


CREATE TABLE TWEETS (
  twit_id INT AUTO_INCREMENT  PRIMARY KEY,
  user_id INT NOT NULL,
  follow_id INT NOT NULL,
  msg_txt VARCHAR(140) DEFAULT NULL,
  pub_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


CREATE TABLE FOLLOWS (
  follow_id INT AUTO_INCREMENT  PRIMARY KEY,
  follow_name VARCHAR(140) NOT NULL
);

ALTER TABLE TWEETS ADD CONSTRAINT fk_user_id FOREIGN KEY (user_id) REFERENCES USERS(user_id);
ALTER TABLE TWEETS ADD CONSTRAINT fk_follow_id FOREIGN KEY (follow_id) REFERENCES FOLLOWS(follow_id);

